    public void controlarCaixa(boolean ativo, Funcionario funcionario) {
        txtCodigo.setEnabled(ativo);
        txtQuantidade.setEnabled(ativo);
        btnSair.setEnabled(ativo);

        jMenuBar1.setEnabled(ativo);
        btnLoginSistema.setEnabled(!ativo);
        for (int i = 0; i < jMenuBar1.getMenuCount(); i++) {
            jMenuBar1.getMenu(i).setEnabled(ativo);
        }

        if (ativo) {
            this.setTitle("Mercadinho Compre Muito - PDV ABERTO - Operador: " + funcionario.getNome());
            txtDescricao.setText("");
            txtCodigo.requestFocus();
            lblTotal.setText("R$ 0,00");
            lblTotal.setForeground(new Color(0, 153, 51));

            btnLoginSistema.setText("Trocar Operador");
            btnLoginSistema.setBackground(new Color(0, 204, 0));
            btnLoginSistema.setForeground(Color.WHITE);
            btnLoginSistema.setEnabled(true);
            jMenuBar1.setEnabled(ativo);
            txtCodigo.requestFocus();
            txtCpfCliente.setEditable(ativo);
        } else {
            this.setTitle("Mercadinho Compre Muito - PDV FECHADO");
            btnLoginSistema.setFont(new java.awt.Font("Dialog", 1, 18));
            btnLoginSistema.setText("Entrar");
            txtCodigo.setText("");
            txtDescricao.setText("CAIXA FECHADO");
            lblEstoque.setText("");
            txtValor.setText("");
            txtQuantidade.setText("");
            lblTotal.setText("Bloqueado");
            lblTotal.setForeground(java.awt.Color.GRAY);
            txtCpfCliente.setEditable(ativo);
            btnSair.setEnabled(true);

            try {
                DefaultTableModel modelo = (DefaultTableModel) tblItens.getModel();
                modelo.setRowCount(0);
            } catch (Exception e) {
                // Ignora o erro caso a tabela não tenha sido iniciada
            }

            btnLoginSistema.setEnabled(true);
        }
    }

