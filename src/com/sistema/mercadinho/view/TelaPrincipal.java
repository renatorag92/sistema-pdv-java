    private void txtQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtQuantidadeActionPerformed
        if (produtoSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Por favor, pesquise um produto pelo código primeiro.");
            txtCodigo.requestFocus();
            return;
        }

        try {
            int qtdDesejada = Integer.parseInt(txtQuantidade.getText());

            // Impede quantidade zero ou negativa
            if (qtdDesejada <= 0) {
                JOptionPane.showMessageDialog(this, "A Quantidade deve ser maior que zero.");
                return;
            }

            // Tenta realizar a venda na lógica do objeto
            boolean vendaFoiPossivel = produtoSelecionado.realizarVenda(qtdDesejada);

            if (vendaFoiPossivel) {
                // estoque.atualizar(produtoSelecionado);

                // Adiciona ao carrinho de compras
                ItemVenda item = new ItemVenda(produtoSelecionado, qtdDesejada);
                vendaAtual.adicionarItem(item);

                // Atualiza a Tabela
                DefaultTableModel modelo = (DefaultTableModel) tblItens.getModel();
                String valorComMoeda = String.format("R$ %.2f", item.calcularPrecoItem());
                String PrecoComMoeda = String.format("R$ %.2f", item.getProduto().getValorUnitario());

                modelo.addRow(new Object[]{
                    item.getProduto().getCodigo(),
                    item.getProduto().getDescricao(),
                    PrecoComMoeda,
                    item.getQuantidade(),
                    valorComMoeda
                });

                lblTotal.setText(String.format("R$ %.2f", vendaAtual.getValorTotal()));

                txtCodigo.setText("");
                txtDescricao.setText("");
                txtValor.setText("");
                txtQuantidade.setText("");
                lblEstoque.setText("");

                this.produtoSelecionado = null;

                txtCodigo.requestFocus();

            } else {
                JOptionPane.showMessageDialog(this, "Estoque insuficiente! Restam apenas: " + produtoSelecionado.getEstoque());
                // Seleciona o texto da quantidade para o usuário digitar outro valor
                txtQuantidade.selectAll();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um número válido (inteiro) na quantidade.");
            txtQuantidade.setText("");
            txtQuantidade.requestFocus();
        }    }//GEN-LAST:event_txtQuantidadeActionPerformed

    private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_F2) {

            if (vendaAtual.getItemVendaProdutos().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Adicione produtos primeiro!");
                return;
            }

            // Pega o texto do CPF 
            String cpfDigitado = txtCpfCliente.getText();

            ClienteDAO dao = new ClienteDAO();
            Cliente clienteEncontrado = dao.consultar(cpfDigitado);

            if (clienteEncontrado != null) {
                vendaAtual.setCliente(clienteEncontrado);
            } else {
                Cliente c = new Cliente();
                c.setCPF(cpfDigitado);
                c.setNome("Consumidor Final");
                vendaAtual.setCliente(c);
            }

            TelaPagamento pagamento = new TelaPagamento(this, true, vendaAtual);
            pagamento.setLocationRelativeTo(this);
            pagamento.setVisible(true);

            if (pagamento.getPgtConfirmado()) {
                ((DefaultTableModel) tblItens.getModel()).setRowCount(0);
                vendaAtual = new Venda();
            }

        }

        if (evt.getKeyCode() == KeyEvent.VK_F1) {
            lblEstoque.setText("");
            ProdutoDAO dao = new ProdutoDAO();
            for (ItemVenda itemVenda : vendaAtual.getItemVendaProdutos()) {
                Produto produto = itemVenda.getProduto();
            }
            ((DefaultTableModel) tblItens.getModel()).setRowCount(0);
            txtCodigo.setText(null);
            txtDescricao.setText(null);
            txtValor.setText(null);
            txtQuantidade.setText(null);
            vendaAtual = new Venda();
            lblTotal.setText("R$ 0,00");
        }

    }//GEN-LAST:event_txtCodigoKeyPressed

    private void tblItensKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblItensKeyReleased
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_DELETE) {
            int linhaSelecionada = tblItens.getSelectedRow();

            if (linhaSelecionada != -1) {

                int resposta = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir?", "Confirmação", JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {

                    vendaAtual.removerItem(linhaSelecionada);
                    DefaultTableModel modelo = (DefaultTableModel) tblItens.getModel();
                    modelo.removeRow(linhaSelecionada);
                    lblTotal.setText(String.format("R$ %.2f", vendaAtual.getValorTotal()));

                }
            }
        }


    }//GEN-LAST:event_tblItensKeyReleased

