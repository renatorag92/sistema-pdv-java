    private void txtCpfClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCpfClienteActionPerformed

        // Não precisa verificar se apertou Enter, esse evento já é o Enter.
        String cpf = txtCpfCliente.getText();

        // Limpa a máscara
        String cpfLimpo = cpf.replace(".", "").replace("-", "").replace(" ", "");

        if (!cpfLimpo.isEmpty()) {
            ClienteDAO dao = new ClienteDAO();
            Cliente clienteEncontrado = dao.consultar(cpf);

            if (clienteEncontrado != null) {
                lblNomeCliente.setText(clienteEncontrado.getNome());
                lblNomeCliente.setForeground(Color.BLUE);

                // Salva numa variável global que será usada para fechar a venda
                this.clienteDaVenda = clienteEncontrado;

                txtCodigo.requestFocus();

            } else {
                int resposta = JOptionPane.showConfirmDialog(this,
                        "Cliente não cadastrado! Deseja cadastrar agora?",
                        "Atenção",
                        JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {
                    TelaCadastroCliente cadastroCli = new TelaCadastroCliente();
                    cadastroCli.setLocationRelativeTo(this);
                    cadastroCli.setVisible(true);
                    txtCodigo.requestFocus();
                } else {
                    //Contunua a venda como anônimo
                    lblNomeCliente.setText("Consumidor Final");
                    lblNomeCliente.setForeground(Color.BLACK); // Volta a cor normal
                    this.clienteDaVenda = null;
                    txtCodigo.requestFocus();
                }
            }
        } else {
            // Se der enter com o CPF vazio, assume que é anônimo e vai pro produto
            txtCodigo.requestFocus();
        }

    }//GEN-LAST:event_txtCpfClienteActionPerformed

