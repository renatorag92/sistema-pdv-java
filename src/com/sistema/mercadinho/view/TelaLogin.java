    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String cpf = txtCpf.getText();
        String senha = new String(txtSenha.getPassword());

        FuncionarioDAO dao = new FuncionarioDAO();
        Funcionario funcionarioLogado = dao.fazerLogin(cpf, senha);

        if (funcionarioLogado != null) {
            JOptionPane.showMessageDialog(this, "Bem vindo, " + funcionarioLogado.getNome() + "!");
            if (funcionarioLogado.getCargo().equals("Gerente")) {
                JOptionPane.showMessageDialog(this, "Acesso administrativo liberado.");
                telaPrincipal.getTxtCodigo().requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Abrindo Caixa...");
            }
            this.dispose();
            telaPrincipal.getTxtCodigo().requestFocus();
            this.telaPrincipal.controlarCaixa(true, funcionarioLogado);
            
        } else {
            JOptionPane.showMessageDialog(this, "Dados invalidos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed


