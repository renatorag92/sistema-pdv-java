    // Método unificado para abrir o cupom
    public void abrirCupomSelecionado() {
        int linha = tblHistorico.getSelectedRow();

        if (linha != -1) {
            // PERIGO RESOLVIDO: Pegamos o objeto da lista usando a linha da tabela
            // (Assumindo que sua listaDeVendas está na mesma ordem da tabela)
            Venda vendaSelecionada = listaDeVendas.get(linha);

            // Usa a classe nova que criamos!
            GeradorCupom.emitir(vendaSelecionada);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma venda na tabela.");
        }
    }

    private void tblHistoricoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHistoricoMouseClicked
        if (evt.getClickCount() == 2) {
            abrirCupomSelecionado();
        }
    }//GEN-LAST:event_tblHistoricoMouseClicked

    private void tblHistoricoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblHistoricoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            evt.consume();
            abrirCupomSelecionado();
        }
    }//GEN-LAST:event_tblHistoricoKeyPressed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblHistorico.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) tblHistorico.getModel();
        if (linhaSelecionada != -1) {
            int idVenda = (int) modelo.getValueAt(linhaSelecionada, 0);
            int resposta = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir a venda #" + idVenda + "?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                // Remove do HashMap
                item = new HistoricoVendaDAO();
                item.remover(idVenda);

                // Remove da tabela visual
                modelo.removeRow(linhaSelecionada);

                // Remove da lista em memória                
                this.listaDeVendas.remove(linhaSelecionada);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma venda para exluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        try {
            String dataTexto = txtFiltroData.getText();

            DateTimeFormatter formatador = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataFiltrada = LocalDate.parse(dataTexto, formatador);

            HistoricoVendaDAO dao = new HistoricoVendaDAO();
            List<Venda> listaFiltrada = dao.filtrarPorData(dataFiltrada);
            
            preencherTabela(listaFiltrada);
            
            // Total vendido no dia
            double totalDia = listaFiltrada.stream().mapToDouble(Venda::getValorTotal).sum();
            JOptionPane.showMessageDialog(this, "Foram encontradas: " + listaFiltrada.size() + " vendas.\nTotal do dia: R$ " + String.format("%.2f", totalDia));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data inválida ou erro ao filtrar! Use dia/mês/ano");
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

