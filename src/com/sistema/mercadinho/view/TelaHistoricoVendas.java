    private void preencherTabela() {
        HistoricoVendaDAO dao = new HistoricoVendaDAO();
        this.listaDeVendas = dao.listar();
        preencherTabela(this.listaDeVendas);
    }

    private void preencherTabela(List<Venda> listaParaExibir) {
        
        this.listaDeVendas = (ArrayList<Venda>) listaParaExibir;
        
        DefaultTableModel modelo = (DefaultTableModel) tblHistorico.getModel();
        modelo.setRowCount(0);
        
        DateTimeFormatter formatador = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");

        for (Venda venda : listaParaExibir) {
            modelo.addRow(new Object[]{
                venda.getId(),
                venda.getHoraDaVenda().format(formatador),
                String.format("R$%.2f", venda.getValorTotal()),
                venda.getFormaPagamento().toString()
            });

        }

    }

